package GradleProject;

public class Person {
    private String personName;
    private int age;
    
    public Person(){
    	
    }

	public String getPersonName() {
		return personName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}
    
}
